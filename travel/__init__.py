# __all__은 Python 모듈에서 다른 모듈이나 패키지로부터 가져올 수 있는 "공개적으로 노출할 심볼들의 목록"을 정의하는 특별한 변수입니다. 
# 모듈이나 패키지의 속성 중에서 외부로 노출하고자 하는 것들을 명시적으로 지정하여 다른 사용자가 모듈을 더 쉽게 사용할 수 있도록 도와줍니다.
# Python에서 모듈을 임포트할 때, from module_name import * 문법을 사용하면 해당 모듈의 __all__에 명시된 심볼들만 현재 네임스페이스로 가져옵니다. 
# 이렇게 함으로써 모듈 작성자가 사용자에게 제공하고자 하는 인터페이스를 명확하게 정의할 수 있습니다.
# 예를 들어, 다음과 같이 mymodule.py 모듈을 정의하고 __all__ 변수를 사용하여 외부로 노출할 심볼들을 지정할 수 있습니다:


__all__ = ["vietnam", "thailand"]